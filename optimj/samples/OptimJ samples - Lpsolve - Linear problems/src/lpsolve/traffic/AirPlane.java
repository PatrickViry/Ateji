package lpsolve.traffic;

/**
 * Air plane j arrives at the airport within the time interval [aj ; bj ]
 *
 */
public class AirPlane {

	final int j;
	final Time a;
	final Time b;
	
	public AirPlane(final int j, final Time a, final Time b) {
		super();
		this.j = j;
		this.a = a;
		this.b = b;
	}
	
	
}
